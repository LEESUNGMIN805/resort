package kr.ac.kopo.resort.vo;

public class PaginationVO {
	
	    
	    private int Start;
	    
	    private int End;
	    
	    private int Prev;
	    
	    private int Last;
	    
	    private int Next;

		public int getStart() {
			return Start;
		}

		public void setStart(int start) {
			Start = start;
		}

		public int getEnd() {
			return End;
		}

		public void setEnd(int end) {
			End = end;
		}

		public int getPrev() {
			return Prev;
		}

		public void setPrev(int prev) {
			Prev = prev;
		}

		public int getLast() {
			return Last;
		}

		public void setLast(int last) {
			Last = last;
		}

		public int getNext() {
			return Next;
		}

		public void setNext(int next) {
			Next = next;
		}

		
	 
	
}
